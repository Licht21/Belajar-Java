public class MathOperation {
    public static void main(String[] args) {
        int a = 100;
        int b = 5;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

//        Augmented Assignment
        int c = 100;

        c += 10;
        System.out.println(c);

        c -= 10;
        System.out.println(c);

        c *= 10;
        System.out.println(c);

        c /= 10;
        System.out.println(c);

        c++;
        System.out.println(c);

        int d = -10;
        System.out.println(d);

        System.out.println(false);
    }
}
