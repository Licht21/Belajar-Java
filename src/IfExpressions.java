public class IfExpressions {
    public static void main(String[] args) {
        int nilai = 70;

        if (nilai <= 70) {
            System.out.println("Silahkan Mengulang");
        } else if (nilai <= 80) {
            System.out.println("Silahkan perbaiki Nilai");
        } else {
            System.out.println("Anda Lulus Dengan Baik");
        }
    }
}
